#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*aux;

	if (lst)
	{
		if (!*lst)
		{
			aux = ft_lstlast(*lst);
			aux -> next = new;
		}
		else
			*lst = new;
	}
}
